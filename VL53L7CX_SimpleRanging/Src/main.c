#include <stdio.h>

TIM_HandleTypeDef htim3;
TIM_HandleTypeDef htim4;

static void MX_GPIO_Init(void);
static void MX_TIM3_Init(void);
static void MX_TIM4_Init(void);

int main(void)
{
  // Reset of all peripherals, Initializes the Flash interface and the Systick.
  HAL_Init();

  // initialize the DWT->CYCCNT clock cycles counter, used for precise timings
  CoreDebug->DEMCR |= CoreDebug_DEMCR_TRCENA_Msk;
  DWT->CYCCNT = 0;
  DWT->CTRL |= DWT_CTRL_CYCCNTENA_Msk;

  // initialize timers for PWM
  MX_GPIO_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  TIM3->CCR1 = 0;
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
  TIM4->CCR1 = 0;
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1);
  uint8_t x=0;
  GPIO_PinState dir=0;
  while (1)
  {
	  x += 1;
	  TIM3->CCR1 = x;
	  TIM4->CCR1 = x;
	  HAL_Delay(10);

	  if (x==0){
		  dir = !dir;
	  }

	  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_10, !dir);
	  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_7, !dir);
	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, dir);
	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, dir);
  }


  // Initialize all configured peripherals
  BSP_COM_Init(COM1);
  MX_TOF_Init();
  MX_MEMS_Init();
  MX_TOF_LoadDefaultConfig();

  // Infinite loop
  while (1)
  {
    MX_TOF_Process();
    MX_MEMS_Process();
  }
}

